numbers = [3, 1, 4, 1, 5, 9, 2]

# numbers[0] : output is 3
# numbers[-1] : output is 2
# numbers[3] : output is 1
# numbers[:-1] : output is [3, 1, 4, 1, 5, 9] (removes one instance of 1)
# numbers[3:4] : output is [1]
# 5 in numbers : output is True
# 7 in numbers : output is False
# "3" in numbers : output is False
# numbers + [6, 5, 3] : output is [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]
